package introductionToJava11AndOOPsConcepts;

public class ArithmeticOperators {
	public static void main(String[] args) {
		int m1 = 20;
		int m2 = 5;
		System.out.println(m1 + m2);
		System.out.println(m1 - m2);
		System.out.println(m1 * m2);
		System.out.println(m1 / m2);
		System.out.println(m1 % m2);
	}

}
